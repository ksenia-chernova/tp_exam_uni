"""
Быстрая сортировка (Quick Sort)
Исходный массив: [45, 67, 12, 23, 9, 101, 23, 555, 13, 72, 87]

Алгоритм работает по принципу "разделяй и властвуй":
1. Выбираем опорный элемент (pivot)
2. Разделяем массив на три части:
   - Элементы меньше опорного
   - Элементы равные опорному  
   - Элементы больше опорного
3. Рекурсивно применяем алгоритм к левой и правой частям

Пример для массива [45, 67, 12, 23, 9, 101, 23, 555, 13, 72, 87]
Выбираем опорный элемент (например, последний: 87)

Разделение:
Левая часть (< 87): [45, 67, 12, 23, 9, 23, 13, 72]
Опорный элемент: [87]
Правая часть (> 87): [101, 555]

Рекурсивно сортируем левую и правую части
"""

def func(arr):

    if len(arr) <= 1:
        return arr

    pivot = arr[len(arr) // 2]

    left = [x for x in arr if x < pivot]
    middle = [x for x in arr if x == pivot]
    right = [x for x in arr if x > pivot]

    return func(left) + middle + func(right)

array = [45, 67, 12, 23, 9, 101, 23, 13, 72, 87]
print(array)
sorted_array = func(array)
print(sorted_array)