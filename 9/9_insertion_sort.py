"""
Сортировка вставками
Исходный массив: [45, 67, 12, 23, 9, 101, 23, 555, 13, 72, 87]

Алгоритм делит массив на отсортированную (слева) и неотсортированную (справа) части.
На каждом шаге берем первый элемент из неотсортированной части и вставляем его 
в правильное место отсортированной части, сдвигая элементы при необходимости.

Шаг 1: [45, | 67, 12, 23, 9, 101, 23, 555, 13, 72, 87]
       67 > 45, оставляем на месте
       Результат: [45, 67, | 12, 23, 9, 101, 23, 555, 13, 72, 87]

Шаг 2: [45, 67, | 12, 23, 9, 101, 23, 555, 13, 72, 87]
       12 < 67 → сдвигаем 67: [45, 67, 67, | 23, 9, 101, 23, 555, 13, 72, 87]
       12 < 45 → сдвигаем 45: [45, 45, 67, | 23, 9, 101, 23, 555, 13, 72, 87]
       Вставляем 12 на позицию 0: [12, 45, 67, | 23, 9, 101, 23, 555, 13, 72, 87]

Шаг 3: [12, 45, 67, | 23, 9, 101, 23, 555, 13, 72, 87]
       23 < 67 → сдвигаем 67: [12, 45, 67, 67, | 9, 101, 23, 555, 13, 72, 87]
       23 < 45 → сдвигаем 45: [12, 45, 45, 67, | 9, 101, 23, 555, 13, 72, 87]
       23 > 12 → вставляем 23 на позицию 1: [12, 23, 45, 67, | 9, 101, 23, 555, 13, 72, 87]

И так далее, пока весь массив не будет отсортирован.
"""

def func(arr):
    n = len(arr)
    for i in range(1, n):
        a = arr[i]
        j = i-1

        while j>=0 and arr[j] > a:
            arr[j+1] = arr[j]
            j -= 1
        
        arr[j+1] = a
        print(arr)
    return arr
    
array = [45, 67, 12, 23, 9, 101, 23, 555, 13, 72, 87]
print(array)
sorted_array = func(array)
print(sorted_array)