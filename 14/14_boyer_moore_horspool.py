def build_table(substring):
    m = len(substring)
    table = {}
    
    for i in range(m - 1):
        table[substring[i]] = m - 1 - i

    # Последнему символу присваиваем длину подстроки
    # Но если он уже есть в таблице, оставляем его старое значение
    if substring[m - 1] not in table:
        table[substring[m - 1]] = m
    
    return table

def boyer_moore_horspool(string, substring, table):
    n = len(string)          # Длина строки (где ищем)
    m = len(substring)       # Длина подстроки (что ищем)
    
    # Проверка крайних случаев
    if m == 0 or n == 0 or m > n:
        return []  # Возвращаем пустой список - вхождений нет
    
    results = []  # Здесь будем хранить индексы начала вхождений
    i = 0        # Текущая позиция в строке, с которой сравниваем подстроку
    
    # Основной цикл - идем по строке
    while i <= n - m:
        """
        Пример для string="БОЛЬШИЕ_МЕТЕОДАННЫЕ" (n=20), substring="ДАННЫЕ" (m=6):
        i может быть от 0 до 14 (20-6), так как подстроку из 6 символов 
        можно разместить только начиная с позиций 0-14
        """
        
        # Начинаем сравнение с конца подстроки (m-1)
        j = m - 1  # j = 5 (последний индекс подстроки "ДАННЫЕ")
        
        # Сравниваем символы с конца
        while j >= 0 and string[i + j] == substring[j]:
            """
            Пример при i=14:
            Сравниваем с конца подстроки:
            j=5: string[19]="Е" == substring[5]="Е" ✓
            j=4: string[18]="Ы" == substring[4]="Ы" ✓
            j=3: string[17]="Н" == substring[3]="Н" ✓
            j=2: string[16]="Н" == substring[2]="Н" ✓
            j=1: string[15]="А" == substring[1]="А" ✓
            j=0: string[14]="Д" == substring[0]="Д" ✓
            Все совпали - нашли вхождение!
            """
            j -= 1  # Переходим к предыдущему символу
        
        # После цикла сравнения:
        if j < 0:
            """
            Если j стал отрицательным - значит все символы совпали
            Мы дошли до j=-1, потому что сравнивали все 6 символов
            """
            results.append(i)  # Запоминаем позицию вхождения
            i += 1             # Сдвигаемся на 1 символ для поиска следующего вхождения
        else:
            """
            Если j >= 0 - значит нашли несовпадение
            Например, при i=0:
            string[0+5]="И" != substring[5]="Е"
            j остался равен 5
            """
            # Берем символ из строки, который находился под последним символом подстроки
            # string[i + m - 1] - это символ в строке, который был бы на месте последнего символа подстроки
            char = string[i + m - 1]
            
            """
            Пример при i=0:
            char = string[0+6-1] = string[5] = "И"
            
            Смотрим в таблицу смещений:
            table = {'Д':5, 'А':4, 'Н':2, 'Ы':2, 'Е':1, '*':6}
            
            Для символа "И" в таблице нет записи (это "*"),
            поэтому table.get("И", m) вернет 6
            """
            skip = table.get(char, m)  # Берем смещение из таблицы или длину подстроки
            
            """
            Пример таблицы из вашего объяснения:
            5 4 2 2 1 6 6
            Д А Н Н Ы Е *
            
            Правило:
            1. Если символ char есть в таблице (один из Д,А,Н,Ы,Е) - берем его значение
            2. Если символа нет в таблице (любой другой символ) - берем длину подстроки (m=6)
            
            Для i=0: char="И" нет в таблице → skip=6
            i += 6 → i=6 (пропускаем 6 символов)
            
            Для i=6: char=string[11]="_" нет в таблице → skip=6
            i += 6 → i=12
            
            Для i=12: char=string[17]="Ы" есть в таблице (значение 1) → skip=1
            i += 1 → i=13
            """
            i += skip  # Сдвигаем подстроку вправо
    
    return results  # Возвращаем все найденные позиции

string = input()
